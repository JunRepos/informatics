/* ─── config 병합 헬퍼 (선생님 편집본 우선) ─── */
function mlaCfg(cid){ return (typeof MLA_CONFIG === 'object' && MLA_CONFIG && MLA_CONFIG[cid]) || {}; }
function mlaEffIntro(cid){ const c = mlaCfg(cid); return (c.intro != null && String(c.intro).trim()) ? c.intro : MLA_INTRO_DEFAULT; }
function mlaEffSituations(cid){
  const ov = mlaCfg(cid).situations || {};
  return MLA_SITUATIONS.map(s => ({ ...s, ...(ov[s.id] || {}) }));
}
function mlaEffSit(cid, id){ return mlaEffSituations(cid).find(s => s.id === id) || null; }
// 라벨용(편집본 title 반영)
function mlaSituationById(id, cid){
  if(cid != null) return mlaEffSit(cid, id);
  return MLA_SITUATIONS.find(s => s.id === id) || null;
}
// 문항별 학생 루브릭: 칸 단위로 교사 편집본 우선, 없으면 기본값. qkey='q1'|'q2'|'q3'
function mlaRubricFor(cid, qkey){
  const ov = (mlaCfg(cid).rubric || {})[qkey] || {};
  const def = MLA_RUBRIC_DEFAULT[qkey] || {};
  const o = {};
  for(const pk of MLA_RUBRIC_LEVELS) o[pk] = (ov[pk] != null && String(ov[pk]).trim()) ? String(ov[pk]) : (def[pk] || '');
  return o;
}
